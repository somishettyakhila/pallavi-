package teegala;

public class DataTypeExample {
public static void main(String args[]) {
	int number = 10;
	double price = 99.99;
	char grade = 'A';
	boolean status = true;
	System.out.println("Integer: " + number);
	System.out.println("Double: " + price);
	System.out.println("Char: " + grade);
	System.out.println("Boolean: " + status);
}
}
